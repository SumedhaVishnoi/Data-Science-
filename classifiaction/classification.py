'''
classification is a supervised learning task where the target variable (Y) is categorical instead of continuous.

In Regression → we predict continuous values (e.g., house price, temperature).

In Classification → we predict discrete categories (e.g., spam/not spam, disease yes/no).

Types of Classification

Binary Classification → Only 2 possible classes (Yes/No, True/False, 0/1).

Multiclass Classification → More than 2 classes (Dog, Cat, Horse, etc.).

Multilabel Classification → Multiple labels can apply to one sample (e.g., a movie can be Action + Comedy + Romance).

for evaluation- accuracy, precision, recall, F1-score, ROC-AUC are commonly used metrics.

'''